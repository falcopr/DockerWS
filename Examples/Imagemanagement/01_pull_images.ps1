# Pull images from a docker registry (Docker Hub is the default)

# Linux images 
docker pull node
docker pull node:alpine
docker pull redis
docker pull nginx
docker pull debian
docker pull microsoft/dotnet
docker pull --all-tags microsoft/dotnet

# Windows images
docker pull microsoft/nanoserver
docker pull microsoft/mssql-server-windows
docker pull microsoft/aspnet
docker pull microsoft/dotnet
docker pull microsoft/iis